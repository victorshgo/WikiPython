n = int(input("Digite um número: "))

if(n==1):
    print("1 - Domingo")
elif(n==2):
    print("2 - Segunda")
elif(n==3):
    print("3 - Terça")
elif(n==4):
    print("4 - Quarta")
elif(n==5):
    print("5 - Quinta")
elif(n==6):
    print("6 - Sexta")
elif(n==7):
    print("7 - Sábado")
else:
    print("Valor inválido!")
